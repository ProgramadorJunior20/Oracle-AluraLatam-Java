
public class EjercicioTablaMultiplicacion {
	
	public static void main(String[] args) {
		
		for (int contador = 1; contador <= 10; contador++) {
			for (int multi = 1; multi <= 10; multi++) {
				System.out.print(contador * multi);
				System.out.print(" ");
			}
			System.out.println();
		}
	}

}
